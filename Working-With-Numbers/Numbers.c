#include <stdio.h>
#include <stdlib.h>
#include <math.h>

int main()
{
//Basic Operations
	printf("%f \n", 8.9);        // Printing a number
	printf("%f \n", 5.0 + 4.5);  // Addition
	printf("%f \n", 5.0 - 4.5);  // Subtraction
	printf("%f \n", 5.0 / 4.5);  // Division
	printf("%f \n", 5.0 * 4.5);  // Multiplication

//Advanced Operations

	printf("%f \n", pow(3, 3));      //Raising to Powers/Exponents
	printf("%f \n", sqrt(64));       //Square Roots
	printf("%f \n", ceil(36.5632));  //Rounding up
        printf("%f \n", floor(4.344));   //Rounding down
return 0;
}
